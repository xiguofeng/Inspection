package com.xgf.inspection.entity;

public class UploadValue {

	private String UserPhoneCode;

	private String QRcode;

	private String SerialNumber;

	private String FileSN;

	private String FileContent;
	
	private String FileLocalUrl;

	public String getUserPhoneCode() {
		return UserPhoneCode;
	}

	public void setUserPhoneCode(String userPhoneCode) {
		UserPhoneCode = userPhoneCode;
	}

	public String getQRcode() {
		return QRcode;
	}

	public void setQRcode(String qRcode) {
		QRcode = qRcode;
	}

	public String getSerialNumber() {
		return SerialNumber;
	}

	public void setSerialNumber(String serialNumber) {
		SerialNumber = serialNumber;
	}

	public String getFileSN() {
		return FileSN;
	}

	public void setFileSN(String fileSN) {
		FileSN = fileSN;
	}

	public String getFileContent() {
		return FileContent;
	}

	public void setFileContent(String fileContent) {
		FileContent = fileContent;
	}

	public String getFileLocalUrl() {
		return FileLocalUrl;
	}

	public void setFileLocalUrl(String fileLocalUrl) {
		FileLocalUrl = fileLocalUrl;
	};

	
}
